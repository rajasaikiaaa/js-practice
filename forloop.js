//creating a program that gives output from 1 to 10 using for loop
for (let num = 1; num <= 10; num++){  //for (initialization; condition; updation)
    console.log(num);
}

// loop in reverse that prints from 10 to 0

for (let count = 10; count >= 0; count--){
    console.log(count);
}

